// less than test
#include "string.hpp"
#include <cassert>
#include <iostream>





int main() {

    {
        String one = 'a';
        String two = 'b';
        
        bool result = one < two;
        std::cout << "lessthan result: " << result << std::endl;
        
        assert(result == true);
    
    
    
    }



    {
        String one = "applesauce";
        String two = "apple";
        
        bool result = two < one;
        std::cout << "lessthan result: " << result << std::endl;
        
        assert(result == true);
    
    
    
    }


    {
        String one = "abcd";
        String two = "bcde";
        
        bool result = two < one;
        std::cout << "lessthan result: " << result << std::endl;
        
        assert(result == false);
    
    
    }





    {
        String one;
        String two = 'b';
        
        bool result = one < two;
        std::cout << "lessthan result: " << result << std::endl;
        
        assert(result == true);
    
    
    
    }



    {
        String one = "a";
        String two = "bcdasdwrwasws";
        
        bool result = one < two;
        std::cout << "lessthan result: " << result << std::endl;
        
        assert(result == true);
    
    
    
    }
    
    std::cout << "Done testing lessThan" << std::endl;
}





